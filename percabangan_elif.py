# Di sini kita mengecek status nilai yang dimasukan
# Menggunakan percabangan elif

nilai = 55

if nilai <= 30:
    print("Sangat Buruk")
elif nilai <= 50:
    print("Buruk") 
elif nilai <= 70:
    print("Cukup")
elif nilai <= 90:
    print("Baik")
elif nilai <= 100:
    print("Sangat Baik")
    